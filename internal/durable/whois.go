func WhoisService(entry string, t time.Duration) (string, error) {
	parts := strings.Split(entry, ".")
	if len(parts) < 2 {
		return "", fmt.Errorf("invalid domain")
	}

	server, ok := servers[parts[len(parts)-1]]
	if !ok {
		return "", fmt.Errorf("unsupported domain")
	}

	connection, err := net.DialTimeout("tcp", net.JoinHostPort(server, "43"), t)
	if err != nil {
		return "", err
	}
	defer connection.Close()

	_, err = connection.Write([]byte(entry + "\r\n"))
	if err != nil {
		return "", err
	}

	buffer, err := io.ReadAll(connection)
	if err != nil {
		return "", err
	}

	return string(buffer), nil
}
